// Language management
let currentLanguage = 'en';

// Get current language from localStorage or default to English
function getCurrentLanguage() {
    return localStorage.getItem('vidgrb-language') || 'en';
}

// Set current language and save to localStorage
function setCurrentLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('vidgrb-language', lang);
    updateLanguageFlag(lang);
}

// Update language flag emoji
function updateLanguageFlag(lang) {
    const flagElement = document.querySelector('.language-flag');
    const flags = {
        en: '🇺🇸',
        es: '🇪🇸',
        fr: '🇫🇷',
        pt: '🇵🇹',
        ar: '🇸🇦',
        ja: '🇯🇵'
    };
    if (flagElement) {
        flagElement.textContent = flags[lang] || '🇺🇸';
    }
}

// Get translation for a key
function getTranslation(key) {
    if (!translations[currentLanguage] || !translations[currentLanguage][key]) {
        return translations.en[key] || key;
    }
    return translations[currentLanguage][key];
}

// Update all translatable elements
function updateTranslations() {
    // Update elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key);
        if (translation) {
            element.textContent = translation;
        }
    });
    
    // Update placeholder texts
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translation = getTranslation(key);
        if (translation) {
            element.placeholder = translation;
        }
    });
    
    // Update document title
    document.title = `VidGrb - ${getTranslation('download_videos')} ${getTranslation('any_platform')}`;
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
    
    // Handle RTL languages
    if (currentLanguage === 'ar') {
        document.documentElement.dir = 'rtl';
        document.body.style.fontFamily = "'Noto Sans Arabic', 'Inter', sans-serif";
    } else {
        document.documentElement.dir = 'ltr';
        document.body.style.fontFamily = "'Inter', -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif";
    }
}

// Initialize translations
function initTranslations() {
    currentLanguage = getCurrentLanguage();
    
    // Set language selector value
    const languageSelect = document.getElementById('language-select');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
    }
    
    // Update translations
    updateTranslations();
    
    // Add language change event listener
    if (languageSelect) {
        languageSelect.addEventListener('change', function() {
            setCurrentLanguage(this.value);
            updateTranslations();
        });
    }
}

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = {
        translations,
        getTranslation,
        setCurrentLanguage,
        getCurrentLanguage,
        updateTranslations,
        initTranslations
    };
}
